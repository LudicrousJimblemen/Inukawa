/// <summary>
/// Defines specific information about a single <see cref="Entity"/>.
/// </summary>
public class Identity {
	/// <summary>
	/// The name of the <see cref="Entity"/>.
	/// </summary>
	public string Name;

	/// <summary>
	/// The noun forms of this <see cref="Identity"/>.
	/// </summary>
	public Cases Cases;
}